SVec3f GraviationField :: Compute(double& U, // potential
                                  const SVec3f& ss_coord // position of spaceship
                                  ) const 
{
  SVec3f ret_var(0.0, 0.0, 0.0);
  SVec3f X(0.0, 0.0, 0.0);
  double RO = ss_coord.x * ss_coord.x + ss_coord.y * ss_coord.y;
  double RV = sqrt(ss_coord.z * ss_coord.z + RO);
  RO = sqrt(RO);
  // Нормируем вектор из ЦЗ на аппарат
        
  X = ss_coord / RV;
        
  double cosL, sinL;

  if (RO > eps)
  {
      cosL = ss_coord.x / RO;
      sinL = ss_coord.y / RO;
  }
  else
  {
      cosL = 1;
      sinL = 0;
  }

  RO /= RV;
  double R = Rad / RV;
  U = 0.0;
  double UU = 0.0;
  double U1 = 0.0;
  double VV = 0.0;
  double cosK1L = 1.0;
  double sinK1L = 0.0;
  double cosKL = cosL;
  double sinKL = sinL;
  double PKK = 1.0;
  int NPN = 2, K = 1, NPK1 = 1;
  int N, N1, NPK, N21;
  double DU;
  double V = 0.0;
  double RK = R;
  double VK = 0.0;
  double UK = 0.0;
  double RN = RK;
  double P1 = 0.0;
  double P = PKK;


  if (fabs(GravityConstant) > eps)
  {

      do
      {
          VK = 0.0;
          UK = 0.0;
          RN = RK;
          P1 = 0.0;
          P = PKK;
          N = K;
          N1 = N + 1;
          NPK = NPN;
          do
          {
              DU = RN * P;
              U1 += (EG_coeff[NPK1] * cosK1L + EG_coeff[NPK1 + 1] * sinK1L) * DU;
              VK += (EG_coeff[NPK] * sinKL - EG_coeff[NPK + 1] * cosKL) * DU;
              DU = (EG_coeff[NPK] * cosKL + EG_coeff[NPK + 1] * sinKL) * DU;
              UK += DU;
              V += N1 * DU;
              if (N == NHarm)
                  break;
              RN *= R;
              DU = P1;
              P1 = P;
              N21 = N + N1;
              P = (N21 * X[2] * P1 - (N + K) * DU) / (N1 - K);
              N = N1;
              N1 = N + 1;
              NPK1 += N21;
              NPK += N21;
          }
          while (N != -1);
          VV += K * VK;
          UU += K * UK;
          U += UK;
          if (K == NHarm)
              break;
          RK *= R;
          cosK1L = cosKL;
          sinK1L = sinKL;
          cosKL = cosK1L * cosL - sinK1L * sinL;
          sinKL = cosK1L * sinL + sinK1L * cosL;
          N21 = K + K + 1;
          PKK = N21 * RO * PKK;
          NPK1 = N21 + NPN;
          NPN = NPK1 + 2;
          K = K + 1;
      }
      while (K != -1);

      U = U * RO;
      V = V * RO;
      RN = R;
      P1 = 1.0;
      P = X[2];
      N1 = 2;
      N = 1;
      NPN = 1;

      do
      {
          DU = EG_coeff[NPN] * P * RN;
          U = DU + U;
          V = N1 * DU + V;
          if (N == NHarm)
              break;
          RN *= R;
          DU = P1;
          P1 = P;
          N21 = N + N1;
          P = (N21 * X[2] * P1 - N * DU) / N1;
          N = N1;
          N1 = N + 1;
          NPN = NPN + N21;
      }
      while (N != -1);
      DU = GravityConstant / RV;
      U = DU * U;
      DU = -DU / RV;
      UU = U1 * RO - UU * X[2];
      ret_var[2] = (V * X[2] - UU * RO) * DU;
      UU = UU * X[2];
      ret_var[0] = (UU * cosL - VV * sinL + V * X[0]) * DU;
      ret_var[1] = (UU * sinL + VV * cosL + V * X[1]) * DU;
  }
  else
  {
      do
      {
          VK = 0.0;
          UK = 0.0;
          P1 = 0.0;
          P = PKK;
          N = K;
          N1 = N + 1;
          NPK = NPN;

          do
          {
              U1 += (EG_coeff[NPK1] * cosK1L + EG_coeff[NPK1 + 1] * sinK1L) * P;
              VK += (EG_coeff[NPK] * sinKL - EG_coeff[NPK + 1] * cosKL) * P;
              UK += (EG_coeff[NPK] * cosKL + EG_coeff[NPK + 1] * sinKL) * P;
              if (N == NHarm)
                  break;
              DU = P1;
              P1 = P;
              N21 = N + N1;
              P = (N21 * X[2] * P1 - (N + K) * DU) / (N1 - K);
              N = N1;
              N1 = N + 1;
              NPK1 = NPK1 + N21;
              NPK = NPK + N21;
          }
          while (N != -1);
          VV += K * VK;
          UU += K * UK;
          U += UK;
          if (K == NHarm)
              break;
          cosK1L = cosKL;
          sinK1L = sinKL;
          cosKL = cosK1L * cosL - sinK1L * sinL;
          sinKL = cosK1L * sinL + sinK1L * cosL;
          N21 = K + K + 1;
          PKK = N21 * RO * PKK;
          NPK1 = N21 + NPN;
          NPN = NPK1 + 2;
          K = K + 1;
      }
      while (K != -1);
      U *= RO;
      P1 = 1.0;
      P = X[2];
      N1 = 2;
      N = 1;
      NPN = 1;
      do
      {
          U += EG_coeff[NPN] * P;
          if (N == NHarm)
              break;
          DU = P1;
          P1 = P;
          N21 = N + N1;
          P = (N21 * X[2] * P1 - N * DU) / N1;
          N = N1;
          N1 = N + 1;
          NPN += N21;
      }
      while (N != -1);
      U *= Rad;
      UU = UU * X[2] - U1 * RO;
      ret_var[2] = -UU * RO * R;
      UU *= X[2];
      ret_var[0] = (UU * cosL + VV * sinL) * R;
      ret_var[1] = (UU * sinL - VV * cosL) * R;
  }
  return ret_var;
}